const STORAGE_KEY = 'smartdash-theme';
const CUSTOM_THEMES_KEY = 'smartdash-custom-themes';

// ── Apply theme to DOM ──────────────────────────────────────
function applyThemeToDOM(theme) {
  const root = document.documentElement;
  if (!theme?.colors) return;

  Object.entries(theme.colors).forEach(([key, value]) => {
    root.style.setProperty(`--color-${key}`, value);
  });

  // Update backwards-compat aliases
  const aliases = {
    '--accent': theme.colors['accent'],
    '--accent-light': theme.colors['accent-muted'],
    '--accent-hover': theme.colors['accent-hover'],
    '--text-primary': theme.colors['text-primary'],
    '--text-heading': theme.colors['text-primary'],
    '--text-secondary': theme.colors['text-secondary'],
    '--text-muted': theme.colors['text-tertiary'],
    '--border': theme.colors['border-default'],
    '--bg-primary': theme.colors['bg-base'],
    '--bg-secondary': theme.colors['bg-surface'],
    '--bg-tertiary': theme.colors['bg-muted'],
    '--bg-card': theme.colors['bg-raised'],
    '--bg-hover': theme.colors['bg-muted'],
    '--success': theme.colors['emerald'],
    '--success-bg': theme.colors['emerald-muted'],
    '--danger': theme.colors['rose'],
    '--danger-bg': theme.colors['rose-muted'],
  };
  Object.entries(aliases).forEach(([prop, val]) => {
    if (val) root.style.setProperty(prop, val);
  });

  // Set color-scheme for native elements
  root.style.colorScheme = theme.category === 'light' ? 'light' : 'dark';
}

// ── Context ─────────────────────────────────────────────────
const ThemeContext = createContext(null);

export function ThemeProvider({ children }) {
  const [customThemes, setCustomThemes] = useState(() => {
    try {
      const stored = localStorage.getItem(CUSTOM_THEMES_KEY);
      return stored ? JSON.parse(stored) : {};
    } catch { return {}; }
  });

  const allThemes = { ...BUILT_IN_THEMES, ...customThemes };

  const [activeThemeId, setActiveThemeId] = useState(() => {
    try {
      return localStorage.getItem(STORAGE_KEY) || 'midnight';
    } catch { return 'midnight'; }
  });

  const activeTheme = allThemes[activeThemeId] || BUILT_IN_THEMES.midnight;

  // Apply theme on mount and change
  useEffect(() => {
    applyThemeToDOM(activeTheme);
  }, [activeThemeId, activeTheme]);

  // Persist active theme ID
  useEffect(() => {
    try { localStorage.setItem(STORAGE_KEY, activeThemeId); } catch {}
  }, [activeThemeId]);

  // Persist custom themes
  useEffect(() => {
    try { localStorage.setItem(CUSTOM_THEMES_KEY, JSON.stringify(customThemes)); } catch {}
  }, [customThemes]);

  const setTheme = useCallback((themeId) => {
    if (allThemes[themeId]) {
      setActiveThemeId(themeId);
    }
  }, [allThemes]);

  const saveCustomTheme = useCallback((theme) => {
    const id = theme.id || `custom_${Date.now()}`;
    const saved = { ...theme, id, builtIn: false };
    setCustomThemes(prev => ({ ...prev, [id]: saved }));
    setActiveThemeId(id);
    return id;
  }, []);

  const deleteCustomTheme = useCallback((themeId) => {
    setCustomThemes(prev => {
      const next = { ...prev };
      delete next[themeId];
      return next;
    });
    if (activeThemeId === themeId) setActiveThemeId('midnight');
  }, [activeThemeId]);

  const previewTheme = useCallback((theme) => {
    applyThemeToDOM(theme);
  }, []);

  const cancelPreview = useCallback(() => {
    applyThemeToDOM(activeTheme);
  }, [activeTheme]);

  return (
    <ThemeContext.Provider value={{
      activeTheme,
      activeThemeId,
      allThemes,
      customThemes,
      setTheme,
      saveCustomTheme,
      deleteCustomTheme,
      previewTheme,
      cancelPreview,
    }}>
      {children}
    </ThemeContext.Provider>
  );
}

export function useTheme() {
  const ctx = useContext(ThemeContext);
  if (!ctx) throw new Error('useTheme must be used within ThemeProvider');
  return ctx;
}
